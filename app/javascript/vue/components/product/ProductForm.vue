    if (topNavSaveButton) {
      topNavSaveButton.removeEventListener('click', () => this.savePricingProduct());
    }
    if (topNavCancelButton) {
      topNavCancelButton.removeEventListener('click', (e) => {
        e.preventDefault();
        this.handleCancel();
      });
    }
  }
};
