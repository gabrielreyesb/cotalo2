            },
            body: JSON.stringify({
              processes_comments: comments
            })
          });
          
          if (!response.ok) {
            console.error('Failed to update processes comments');
          }
        } catch (error) {
          console.error('Error updating processes comments:', error);
        }
      }
    },
    
    handleMaterialSelectedForProducts(materialId) {
      if (!this.product || !this.product.data) return;
      
      // Save the selected material ID in the product data
      this.product.data.selected_material_id = materialId;
      
      // If we have a productId, update on the server
      if (this.productId) {
        try {
          fetch(`/api/v1/products/${this.productId}/update_selected_material`, {
            method: 'PUT',
            headers: {
              'Content-Type': 'application/json',
              'Accept': 'application/json',
              'X-CSRF-Token': this.apiToken,
              'X-Requested-With': 'XMLHttpRequest'
            },
            body: JSON.stringify({
              selected_material_id: materialId
            })
          }).catch(error => {
            console.error('Error updating selected material:', error);
          });
        } catch (error) {
          console.error('Error updating selected material:', error);
        }
      }
    },
    
    handleMaterialCalculationChanged(eventData) {
      console.log('[ProductForm] Received material-calculation-changed event:', eventData);
      
      if (!this.product || !this.product.data) return;
      
      // Update the material in the product data
      const materialIndex = this.product.data.materials.findIndex(m => m.id === eventData.materialId);
      if (materialIndex !== -1) {
        console.log('[ProductForm] Updating material data:', {
          materialId: eventData.materialId,
          totalSheets: eventData.totalSheets,
          totalSquareMeters: eventData.totalSquareMeters,
          totalPrice: eventData.totalPrice
        });
        
        // Update the material data
        this.product.data.materials[materialIndex] = {
          ...this.product.data.materials[materialIndex],
          totalSheets: eventData.totalSheets,
          totalSquareMeters: eventData.totalSquareMeters,
          totalPrice: eventData.totalPrice
        };
      }
      
      // Check if this is the selected material for processes
      if (eventData.materialId === this.product.data.selected_material_id && this.product.data.processes && this.product.data.processes.length > 0) {
        console.log('[ProductForm] Selected material changed, recalculating processes');
        
        // Force recalculation of all processes by marking them with the recalculation flag
        const processesNeedingRecalculation = this.product.data.processes.map(process => {
          return {
            ...process,
            _needsRecalculation: true
          };
        });
        
        // This will trigger the recalculation in updateProcesses
        this.updateProcesses(processesNeedingRecalculation);
      }
      
      // If pricing needs recalculation, update it
      if (eventData.needsPricingRecalculation) {
        console.log('[ProductForm] Recalculating pricing');
        this.recalculatePricing();
      }
    },
    
    handlePricingUpdate(updatedPricing) {
      if (!this.product || !this.product.data) return;
      
      
      // Update the pricing data with the new values
      this.product.data.pricing = {
        ...this.product.data.pricing,
        ...updatedPricing
      };
      
      // Recalculate pricing to update all derived values
      this.recalculatePricing();
      
      // Force update to ensure changes propagate
      this.$nextTick(() => {
        this.$forceUpdate();
      });
    },
    handleCancel() {
      window.location.href = '/products';
    },
    async calculateSuggestedMargin() {
      if (!this.product || !this.product.data || !this.product.data.pricing) {
        return 0;
      }
      
      const pricing = this.product.data.pricing;
      const totalBeforeMargin = pricing.subtotal + pricing.waste_value;
            
      try {
        // Fetch price margins from the API
        const response = await fetch('/api/v1/price_margins', {
          headers: {
            'Accept': 'application/json',
            'X-Requested-With': 'XMLHttpRequest'
          }
        });
        
        if (!response.ok) {
          throw new Error(`Failed to load price margins: ${response.status} ${response.statusText}`);
        }
        
        const priceMargins = await response.json();
        
        // Sort price margins by min_price to ensure we find the highest range
        const sortedMargins = [...priceMargins].sort((a, b) => parseFloat(a.min_price) - parseFloat(b.min_price));
        
        // Find the appropriate price margin based on the total before margin
        const priceMargin = sortedMargins.find(margin => {
          const minPrice = parseFloat(margin.min_price) || 0;
          const maxPrice = parseFloat(margin.max_price) || Infinity;
          const isInRange = totalBeforeMargin >= minPrice && totalBeforeMargin <= maxPrice;
          
          return isInRange;
        });
        
        // If no range matches, use the margin from the highest range
        const suggestedMargin = priceMargin ? 
          parseFloat(priceMargin.margin_percentage) || 0 : 
          parseFloat(sortedMargins[sortedMargins.length - 1]?.margin_percentage) || 0;
        
        return suggestedMargin;
      } catch (error) {
        console.warn('Price margins API not available, using default margin:', error);
        // Use a default margin based on the total price
        if (totalBeforeMargin <= 1000) return 30; // 30% for small orders
        if (totalBeforeMargin <= 5000) return 25; // 25% for medium orders
        return 20; // 20% for large orders
      }
    },
    async updateIncludeExtrasInSubtotal(value) {
      if (!this.product || !this.product.data) return;
      
      // Update locally
      this.product.data.include_extras_in_subtotal = value;
      
      // Recalculate pricing
      this.recalculatePricing();
      
      // If we have a productId, also update on the server
      if (this.productId) {
        try {
          const response = await fetch(`/api/v1/products/${this.productId}/update_include_extras_in_subtotal`, {
            method: 'PUT',
            headers: {
              'Content-Type': 'application/json',
              'Accept': 'application/json',
              'X-CSRF-Token': this.apiToken,
              'X-Requested-With': 'XMLHttpRequest'
            },
            body: JSON.stringify({
              include_extras_in_subtotal: value
            })
          });
          
          if (!response.ok) {
            console.error('Failed to update include extras in subtotal preference');
          }
        } catch (error) {
          console.error('Error updating include extras in subtotal preference:', error);
        }
      }
    }
  },
